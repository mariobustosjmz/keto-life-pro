const strings = { en, es };
let currentLang = 'es';

const i18n = {
  setLang(lang) {
    if (strings[lang]) {
      currentLang = lang;
      document.documentElement.lang = lang;
      return true;
    }
    return false;
  },
  getLang() {
    return currentLang;
  },
  t(key, params = {}) {
    const keys = key.split('.');
    let value = strings[currentLang];
    for (const k of keys) {
      if (value == null) return key;
      value = value[k];
    }
    if (typeof value !== 'string') return key;
    return value.replace(/\{(\w+)\}/g, (_, name) => {
      return params[name] != null ? String(params[name]) : `{${name}}`;
    });
  },
  _raw: strings
};

window.i18n = i18n;
